
notaA = float(input("Digite sua primeira nota: "))

notaB = float(input("Digite sua segunda nota:"))

mediafinal = (notaA + notaB) / 2

if mediafinal >= 6.0:
        print("Você está aprovado" , "\n Média:" , mediafinal)

else:
        print("Você está reprovado" ,"\n Média:",  mediafinal)

